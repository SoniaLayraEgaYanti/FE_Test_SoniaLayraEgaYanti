    playPauseButton.className = "fas fa-pause";
  });

  audio.addEventListener('pause', function () {
    playPauseButton.className = "fas fa-play";
  });

  function togglePlayPause() {
    if (audio.paused) {
      audio.play();
    } else {
      audio.pause();
    }
  }
